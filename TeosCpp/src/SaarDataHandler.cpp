/** calculate the data position offset within an array */
unsigned SaarDataHandler::getOffset(unsigned arrayNumber, bool isSaar)
{
   unsigned offset = 0;

   if (arrayNumber > 1)
   {
      unsigned offsetArrayNumber = arrayNumber;

      while (offsetArrayNumber > 1)
      {
         if (offsetArrayNumber > 1) offsetArrayNumber -= 2;

         if (isSaar)
         {
            offset += saarArrayInfo[offsetArrayNumber].elements;
         }
         else
         {
            offset += deltaSaArrayInfo[offsetArrayNumber].elements;
         }
      }
   }

   return offset;
}

/**********************************
   calculate the data position of
   an array value within a saar
   or a delta_sa array
   ------------------------------
   arraypos is the position in the
   original (184275 element) array
***********************************/
double SaarDataHandler::getSaarValue(unsigned valpos)
{
	double sval = 0.0;

	/** read the "map" of the array */
	unsigned arrayNumber = getSaarArrayPos(valpos);
	ARRAYINFO sinfo = saarArrayInfo[arrayNumber];

	/*************************************
      An even numbered "arrayNumber"
      means a section of now nonexistent
      "8.9999999999999998e+90" values
      ---------------------------------
      i.e. "sinfo.wasRepeatValue" == true
	**************************************/
	if (sinfo.wasRepeatValue)
	{
		sval = repeatSectionValue;
	}
	else
	{
	   /** "readPos" is the new location */
		unsigned readPos = valpos - sinfo.begins;
      readPos += getOffset(arrayNumber, true);

		/** get saar array element value */
		sval = saar[readPos];
	}

	return sval;
}

/** access the map to the data position within the saar array */
unsigned SaarDataHandler::getSaarArrayPos(unsigned valPos)
{
	unsigned sarrpos = maxSAARCOUNT;

	for (unsigned apos = 0; apos < maxSAARCOUNT; apos++)
	{
		ARRAYINFO sinfo = saarArrayInfo[apos];
		if (sinfo.ends >= valPos)
		{
			sarrpos = apos;
			break;
		}
	}

	return sarrpos;
}

/**********************************
   calculate the data position of
   an array value within a saar
   or a delta_sa array
   ------------------------------
   arraypos is the position in the
   original (184275 element) array
***********************************/
double SaarDataHandler::getDeltaSaValue(unsigned valpos)
{
	double dval = 0.0;

	/** read the "map" of the array */
	unsigned arrayNumber = getDeltaSaArrayPos(valpos);
	ARRAYINFO dinfo = deltaSaArrayInfo[arrayNumber];

	/*************************************
      An even numbered "arrayNumber"
      means a section of now nonexistent
      "8.9999999999999998e+90" values
      ---------------------------------
      i.e. "dinfo.wasRepeatValue" == true
	**************************************/
	if (dinfo.wasRepeatValue)
	{
		dval = repeatSectionValue;
	}
	else
	{
      /** "readPos" is where the new location is */
		unsigned readPos = valpos - dinfo.begins;
      readPos += getOffset(arrayNumber, false);

		/** get deltasa array element value */
		dval = deltasa[readPos];
	}

	return dval;
}

